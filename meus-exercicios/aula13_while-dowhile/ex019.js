// WHILE (Executa enquanto a condição for verdadeira)
var cont = 1
while (cont <= 3) {
    console.log(`Usando WHILE ${cont}`)
    cont++
}

// DO WHILE (Executa no mínimo 1x, independente se a condição for true ou false)
cont = 1
do {
    console.log(`Usando DO WHILE ${cont}`)
    cont++
} while (cont <= 3)